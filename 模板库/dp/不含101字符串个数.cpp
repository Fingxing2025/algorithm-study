#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

/*
  小状态计数 DP：统计长度为 n 的二进制串中，不含子串 101 的方案数。

  这类题的关键不是“长度”，而是“末尾最近几位长什么样”。
  因为当你决定下一位填 0 还是 1 时，是否会形成 101，只和最后两位有关。

  这里用四组状态表示长度为 i 时，末尾两位属于哪种形态。
  虽然变量名较简略，但本质就是在维护一个 4 状态自动机。
*/

int main()
{
	// tt / tf / ft / ff 分别表示当前长度下，末尾两位的四种状态计数。
	int tt[25], tf[25], ft[25], ff[25], a[25];
	a[1] = 2;
	a[2] = 4;
	// 长度为 2 时，四种末尾状态各出现 1 次：11、10、01、00。
	tt[2] = tf[2] = ft[2] = ff[2] = 1;

	// 按照“末尾补 0 或 1 后会转移到哪一种末尾形态”进行递推。
	// a[i] 记录长度为 i 的总合法方案数。
	for (int i = 3; i <= 20; ++i)
	{
		// 新串末尾想变成 11，上一位必须先是 1，再补一个 1；
		// 所以前一状态只能来自 11 或 10。
		tt[i] = tt[i - 1] + tf[i - 1];
		// 新串末尾想变成 10，前一位必须先是 0，再补一个 1；
		// 又因为不能出现 101，所以前一状态只能从 00 转来，不能从 01 转来。
		tf[i] = ff[i - 1];
		// 新串末尾想变成 01，说明上一位是 1、当前位补 0；
		// 前一状态可以是 11 或 10。
		ft[i] = tt[i - 1] + tf[i - 1];
		// 新串末尾想变成 00，说明上一位是 0、当前位也补 0；
		// 前一状态可以是 01 或 00。
		ff[i] = ft[i - 1] + ff[i - 1];
		// 总方案数就是四种合法末尾状态的总和。
		a[i] = tt[i] + tf[i] + ft[i] + ff[i];
	}
	int n;
	while (true)
	{
		cin >> n;
		if (n == -1)
			break;
		cout << a[n] << "\n";
	}
}
